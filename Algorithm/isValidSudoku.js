/**
 * 有效得数独
 * @param {character[][]} board
 * @return {boolean}
 * 请你判断一个 9 x 9 的数独是否有效。只需要 根据以下规则 ，验证已经填入的数字是否有效即可。
    数字 1-9 在每一行只能出现一次。
    数字 1-9 在每一列只能出现一次。
    数字 1-9 在每一个以粗实线分隔的 3x3 宫内只能出现一次。（请参考示例图）
 */
var isValidSudoku = function (board) {
    for (let i = 0; i < 9; i++) {
        let obj = {};
        let j = 0;
        while (j < 9) {
            if (!obj[board[i][j]]) {
                obj[board[i][j]] = true;
            } else if (obj[board[i][j]] && board[i][j] !== ".") {
                return false;
            }
            j++;
        }
        let left = 0;
        let obj2 = {};
        while (left < 9) {
            if (!obj2[board[left][i]]) {
                obj2[board[left][i]] = true;
            } else if (obj2[board[left][i]] && board[left][i] !== ".") {
                return false;
            }
            left++;
        }
    }
    for (let i = 0; i < 9; i++) {
        for (let j = 0; j < 9; j++) {
            if (i % 3 == 0 && j % 3 == 0) {
                let l = i,
                    r = j;
                let obj4 = {};
                for (let i = l; i < l + 3; i++) {
                    for (let j = r; j < r + 3; j++) {
                        if (!obj4[board[i][j]]) {
                            obj4[board[i][j]] = true;
                        } else if (obj4[board[i][j]] && board[i][j] !== ".") {
                            return false;
                        }
                    }
                }
            }
        }

    }
    return true;
};

